function numToColLetter(num: number) {
    let colLetter = "";
    while (num > 0) {
        let remainder = (num - 1) % 26;
        colLetter = String.fromCharCode(65 + remainder) + colLetter;
        num = Math.floor((num - 1) / 26);
    }
    return colLetter;
}

